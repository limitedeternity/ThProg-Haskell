f:: Int -> String
f n | n>=1000 = "M" ++ f (n-1000)
    | n>=500  = "D" ++ f (n-500)
    | n>=100  = "C" ++ f (n-100)
    | n>=50   = "L" ++ f (n-50)
    | n>=10   = "X" ++ f (n-10)
    | n>=5    = "V" ++ f (n-5)
    | n>=1    = "I" ++ f (n-1)
    | True    = ""

